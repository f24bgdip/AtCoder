#include <stdio.h>

int main() {
  long long values[] = {
      1,                 // 1^1
      4,                 // 2^2
      27,                // 3^3
      256,               // 4^4
      3125,              // 5^5
      46656,             // 6^6
      823543,            // 7^7
      16777216,          // 8^8
      387420489,         // 9^9
      10000000000,       // 10^10
      285311670611,      // 11^11
      8916100448256,     // 12^12
      302875106592253,   // 13^13
      11112006825558016, // 14^14
      437893890380859375 // 15^15
  };

  long long B;

  if (scanf("%lld", &B) != 1)
    return -1;

  int i;
  for (i = 0; i < 15; i++) {
    if (values[i] == B)
      break;
  }

  if (i >= 15) {
    printf("-1\n");
  } else {
    printf("%d\n", i + 1);
  }

  return 0;
}
